#include<stdio.h>
void main(){

	int x;

	printf("Enter the size of the array:\n");
	scanf("%d",&x);

	int arr[x];

	printf("Enter the Elements of the Array:\n");

	for(int i=0;i<x;i++){
	
		scanf("%d",&arr[i]);
	
	}

	printf("Elements having Index as Similar as Elements are:\n");

	int count=0;

	for(int i=0;i<x;i++){
		
		if(i==arr[i]){
		
			printf("%d\n",arr[i]);
	
		}else{
		
			count++;
	
		}
	}

	if(count>=1){
		
		printf("None\n");
	}
}
